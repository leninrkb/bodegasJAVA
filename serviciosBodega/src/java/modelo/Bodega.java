package modelo;

/**
 *
 * @author leninx
 */
public class Bodega {
    private String bodegaID;
    private String bodegaRUC;
    private String bodega;
    private String ciudad;

    public Bodega() {
    }

    public String getBodegaID() {
        return bodegaID;
    }

    public void setBodegaID(String bodegaID) {
        this.bodegaID = bodegaID;
    }

    public String getBodegaRUC() {
        return bodegaRUC;
    }

    public void setBodegaRUC(String bodegaRUC) {
        this.bodegaRUC = bodegaRUC;
    }

    public String getBodega() {
        return bodega;
    }

    public void setBodega(String bodega) {
        this.bodega = bodega;
    }

    public String getCiudad() {
        return ciudad;
    }

    public void setCiudad(String ciudad) {
        this.ciudad = ciudad;
    }
    
    
}
